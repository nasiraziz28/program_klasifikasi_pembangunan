    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int ok=JOptionPane.showConfirmDialog(null,"Apakah Yakin Menghapus data Klasifikasi Tahun "+txt_tahun.getText()+" ???", "Konfirmasi",JOptionPane.YES_NO_CANCEL_OPTION);
       if (ok==0)
       {
        try
         {
            String sql="delete from tbl_kabupaten where tahun='"+txt_tahun.getText()+"'";
            String sql1="delete from tbl_provinsi where tahun='"+txt_tahun.getText()+"'";
            
            PreparedStatement st=con.prepareStatement(sql);
             PreparedStatement sz=con.prepareStatement(sql1);
            st.executeUpdate();
            sz.executeUpdate();
            JOptionPane.showMessageDialog(null, "Hapus Data Sukses");
         }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Hapus Data Gagal");
        }
       }
       tableshow();
 
    }//GEN-LAST:event_btn_hapusActionPerformed

