    private void tableshow(){
    int tahun,tahun_n;
    tahun   = Integer.parseInt(txt_tahun.getText());

     DefaultTableModel kb= new DefaultTableModel();
        kb.addColumn("Kabupaten");
        kb.addColumn("PDRB "+tahun+"");
        tbl_kab.setModel(kb);
        
        DefaultTableModel prn= new DefaultTableModel();
        prn.addColumn("Provinsi");
        prn.addColumn("PDRB "+tahun+"");
        tbl_prov.setModel(prn);
        
        try{
            res=stat.executeQuery("SELECT tbl_kabupaten.kabupaten, tbl_kabupaten.pdrb, tbl_kabupaten.tahun FROM tbl_kabupaten WHERE tahun='"+tahun+"'");
            while (res.next()){
            kb.addRow(new Object[]{
            res.getString("kabupaten"),
            res.getString("pdrb")});
            }
        }catch (Exception e){ JOptionPane.showMessageDialog(null, "Data PDRB Kabupaten/ Kota Tahun "+txt_tahun.getText()+" tidak dimukan");}
        
         try{
            res=stat.executeQuery("SELECT tbl_provinsi.provinsi, tbl_provinsi.pdrb, tbl_provinsi.tahun FROM tbl_provinsi WHERE tahun='"+txt_tahun.getText()+"'");
            while (res.next()){
            prn.addRow(new Object[]{
            res.getString("provinsi"),
            res.getString("pdrb")});
            }
        }catch (Exception e){ JOptionPane.showMessageDialog(null, "Data Tahun PDRB Provinsi Tahun "+txt_tahun.getText()+" tidak dimukan");}
        
    
    
    }
